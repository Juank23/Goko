package org.goko.core.rs274ngcv3.parser;

public class GCodeToken {
	/** The token type */
	private GCodeTokenType type;
	/** The value */
	private String value;
	/**
	 * @param type
	 * @param value
	 */
	public GCodeToken(GCodeTokenType type, String value) {
		super();
		this.type = type;
		this.value = value;
	}
	/**
	 * @return the type
	 */
	public GCodeTokenType getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(GCodeTokenType type) {
		this.type = type;
	}
	/**
	 * @return the value
	 */
	public String getValue() {
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public void setValue(String value) {
		this.value = value;
	}
	/** (inheritDoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}
	/** (inheritDoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		GCodeToken other = (GCodeToken) obj;
		if (value == null) {
			if (other.value != null) {
				return false;
			}
		} else if (!value.equals(other.value)) {
			return false;
		}
		return true;
	}

}
